        AND    A.CODE_TYPE = B.CODE_TYPE
        AND    A.CODE_NAME < '20151210'
        AND    B.CODE_NAME >= TO_CHAR(to_date('20151210', 'YYYYMMDD') - 36 DAY, 'YYYYMMDD')
