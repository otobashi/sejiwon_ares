            AND    B.CODE_NAME >= TO_CHAR(to_date('20160208', 'YYYYMMDD') - 393 DAY, 'YYYYMMDD')
            AND    A.CODE_ID   >= B.CODE_ID ) C
    WHERE  A.DIV_CD = B.DIVISION_CODE
