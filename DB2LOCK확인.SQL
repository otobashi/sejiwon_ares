SELECT
 L.DB_NAME,
 L.AGENT_ID,
 L.APPL_NAME,
 L.LOCK_OBJECT_TYPE,
 A.APPL_NAME,
 A.APPL_STATUS,
 A.TPMON_CLIENT_USERID,
 A.TPMON_CLIENT_WKSTN,
 A.TPMON_CLIENT_APP,
 A.TPMON_ACC_STR,
 L.AGENT_ID_HOLDING_LK,
 L.LOCK_WAIT_START_TIME,
 L.LOCK_NAME,
 L.LOCK_MODE
FROM
 SYSIBMADM.LOCKWAITS L
INNER JOIN
 SYSIBMADM.SNAPAPPL_INFO A
ON
 L.AGENT_ID = A.AGENT_ID;
