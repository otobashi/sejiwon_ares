SELECT A11.SUBSDR_CD  SUBSDR_CD
      ,A14.SUBSDR_SHRT_NAME  NEW_SUBSDR_SHRT_NAME
      ,A14.SORT_ORDER  SORT1_ORDER
      ,A11.MGT_ORG_CD  MGT_SUBSDR_CD
      ,A13.MGT_SUBSDR_NAME  MGT_SUBSDR_NAME
      ,A11.DIV_CD  DIV_CD
      ,A12.SCRN_DSPL_SEQ  DIV_KOR_NAME
      ,A12.DIV_SHRT_NAME  DIV_SHRT_NAME
--  A11.PROD_LVL3_CD,
--  A11.PROD_LVL4_CD,
      ,A11.BASE_YYYYMM  BASE_YYYYMM
      ,SUM(A11.PME_BAL_AMT)  WJXBFS1
      ,SUM(A11.NEW_AMT)  WJXBFS2
      ,SUM(A11.INCR_AMT)  WJXBFS3
      ,SUM(A11.DECR_AMT)  WJXBFS4
      ,SUM(A11.CHG_AMT)  WJXBFS5
      ,SUM(A11.BAL_AMT)  WJXBFS6  --BACKLOG AMOUNT
FROM   NPT_APP.NV_DWW_B2B_BAL_H  A11
       LEFT OUTER JOIN  NPT_APP.NV_DWD_DIV_LEAF_M  A12
       ON (A11.DIV_CD = A12.DIV_CD)
       LEFT OUTER JOIN  NPT_APP.NV_DWD_SUBSDR_MGT_M  A13
       ON (A11.MGT_ORG_CD = A13.MGT_SUBSDR_CD)
       LEFT OUTER JOIN  NPT_APP.NV_DWD_SUBSDR_M  A14
       ON (A11.SUBSDR_CD = A14.SUBSDR_CD)
WHERE (A11.BASE_YYYYMM BETWEEN '201511' AND '201511'
AND    A11.CURRENCY_TYPE_CD IN ('USD')
AND    A11.PJT_STG_CD IN ('A'))
GROUP BY A11.SUBSDR_CD
        ,A14.SUBSDR_SHRT_NAME
        ,A14.SORT_ORDER
        ,A11.MGT_ORG_CD
        ,A13.MGT_SUBSDR_NAME
        ,A11.DIV_CD
        ,A12.SCRN_DSPL_SEQ
        ,A12.DIV_SHRT_NAME
--  A11.PROD_LVL3_CD,
--  A11.PROD_LVL4_CD,
        ,A11.BASE_YYYYMM

