SELECT A.CORPORATION_CODE AS SUBSDR_CD
      ,A.BASIS_YYYYWW     AS YYYYWW
      ,A.BASIS_YYYYMM     AS YYYYMM
      ,A.DIVISION_CODE    AS DIV_CD
      ,SUM(A.BEGINNING_AMT
          +A.NEW_AMT
          +A.INCREASE_AMT
          +A.DECREASE_AMT   ) AS AWARD_AMT
      ,SUM(A.CHANGE_AMT     ) AS SALES_AMT
 FROM GWRS_MGR.IPTDW_IFF_GWRS_B2B_PIPELINE_BALANCE A
WHERE A.STAGE_CODE     ='A'     --Award
  AND A.CURRENCY_CODE  = 'USD'  --기준통화
GROUP BY A.CORPORATION_CODE
        ,A.BASIS_YYYYWW
        ,A.BASIS_YYYYMM
        ,A.DIVISION_CODE
WITH UR;